// 计算签号
export function calcQianHao(chars: string[]): number {
  // 简化版：取三个字的笔画数
  const bihua = chars.map(c => getStrokeCount(c))
  const total = bihua.reduce((a, b) => a + b, 0)
  return ((total - 1) % 384) + 1
}

// 获取笔画数（简化版）
function getStrokeCount(char: string): number {
  // 这里使用简化的随机数，实际应用中应使用真实笔画数据库
  const code = char.charCodeAt(0)
  return (code % 20) + 1
}

// 获取签文
export function getQianWen(hao: number): typeof DEFAULT_QIAN {
  return ZHUGE_QIAN[hao] || DEFAULT_QIAN
}
