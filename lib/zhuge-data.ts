export function getZhuGeQian(number: number) {
  const index = (number - 1) % ZHUGE_QIAN_WEN.length;
  return ZHUGE_QIAN_WEN[index];
}

export function getRandomQian() {
  const randomNum = Math.floor(Math.random() * ZHUGE_QIAN_WEN.length) + 1;
  return getZhuGeQian(randomNum);
}

export function calculateQianByInput(input: string): number {
  let sum = 0;
  for (let i = 0; i < input.length; i++) {
    sum += input.charCodeAt(i);
  }
  return (sum % 50) + 1;
}
