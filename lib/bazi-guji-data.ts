// 分析原局干支关系
export function analyzeGanZhiRelations(bazi: {
  yearGan: string, yearZhi: string,
  monthGan: string, monthZhi: string,
  dayGan: string, dayZhi: string,
  hourGan: string, hourZhi: string
}): {
  tianGan: string[]
  diZhi: string[]
  zhengZhu: string[]
} {
  const result = {
    tianGan: [] as string[],
    diZhi: [] as string[],
    zhengZhu: [] as string[]
  }
  
  const gans = [bazi.yearGan, bazi.monthGan, bazi.dayGan, bazi.hourGan]
  const zhis = [bazi.yearZhi, bazi.monthZhi, bazi.dayZhi, bazi.hourZhi]
  const ganZhis = [
    bazi.yearGan + bazi.yearZhi,
    bazi.monthGan + bazi.monthZhi,
    bazi.dayGan + bazi.dayZhi,
    bazi.hourGan + bazi.hourZhi
  ]
  
  // 检查天干关系
  for (const he of GAN_ZHI_RELATIONS.tianGanHe) {
    if (gans.includes(he.gan1) && gans.includes(he.gan2)) {
      result.tianGan.push(he.desc)
    }
  }
  for (const ke of GAN_ZHI_RELATIONS.tianGanKe) {
    if (gans.includes(ke.gan1) && gans.includes(ke.gan2)) {
      result.tianGan.push(ke.desc)
    }
  }
  
  // 检查地支关系
  for (const he of GAN_ZHI_RELATIONS.diZhiLiuHe) {
    if (zhis.includes(he.zhi1) && zhis.includes(he.zhi2)) {
      result.diZhi.push(he.desc)
    }
  }
  for (const sanhe of GAN_ZHI_RELATIONS.diZhiSanHe) {
    const count = sanhe.zhi.filter(z => zhis.includes(z)).length
    if (count >= 2) {
      result.diZhi.push(count === 3 ? sanhe.desc : sanhe.desc.replace("三合", "半合"))
    }
  }
  for (const chong of GAN_ZHI_RELATIONS.diZhiChong) {
    if (zhis.includes(chong.zhi1) && zhis.includes(chong.zhi2)) {
      result.diZhi.push(chong.desc)
    }
  }
  for (const hai of GAN_ZHI_RELATIONS.diZhiHai) {
    if (zhis.includes(hai.zhi1) && zhis.includes(hai.zhi2)) {
      result.diZhi.push(hai.desc)
    }
  }
  
  // 检查整柱组合
  for (const zuhe of ZHENG_ZHU_ZUHE) {
    if (ganZhis.includes(zuhe.ganZhi)) {
      result.zhengZhu.push(`${zuhe.ganZhi}${zuhe.type}`)
    }
  }
  
  return result
}
