// 摇卦
export function castGua(): {
  yaoList: number[]
  guaIndex: number
  bianGuaIndex: number
  bianYaoList: number[]
} {
  const yaoList: number[] = []
  const bianYaoList: number[] = []
  // 从下往上摇六次
  for (let i = 0; i < 6; i++) {
    // 三枚铜钱，每枚正面为3，反面为2
    const result = Math.floor(Math.random() * 4) + 6 // 6-9
    // 6:老阴,7:少阳,8:少阴,9:老阳
    yaoList.push(result)
    // 6和9为变爻
    if (result === 6 || result === 9) {
      bianYaoList.push(i)
    }
  }
  // 计算本卦
  const benGuaYao = yaoList.map(y => y % 2)
  const shangGua = getGuaIndex(benGuaYao.slice(3))
  const xiaGua = getGuaIndex(benGuaYao.slice(0, 3))
  const guaIndex = shangGua * 8 + xiaGua
  // 计算变卦
  const bianGuaYao = benGuaYao.map((y, i) => {
    if (yaoList[i] === 6 || yaoList[i] === 9) {
      return 1 - y
    }
    return y
  })
  const bianShangGua = getGuaIndex(bianGuaYao.slice(3))
  const bianXiaGua = getGuaIndex(bianGuaYao.slice(0, 3))
  const bianGuaIndex = bianShangGua * 8 + bianXiaGua
  return {
    yaoList,
    guaIndex,
    bianGuaIndex,
    bianYaoList
  }
}
function getGuaIndex(yao: number[]): number {
  return BA_GUA.findIndex(g => g.yao[0] === yao[0] && g.yao[1] === yao[1] && g.yao[2] === yao[2])
}
// 获取卦象信息
export function getGuaInfo(index: number) {
  const shangGuaIndex = Math.floor(index / 8)
  const xiaGuaIndex = index % 8
  return {
    name: GUA_MING[index],
    xiang: GUA_XIANG[index],
    ci: GUA_CI[index],
    shangGua: BA_GUA[shangGuaIndex],
    xiaGua: BA_GUA[xiaGuaIndex]
  }
}
// 六爻解释
export function getYaoName(yao: number, position: number): string {
  const positions = ["初", "二", "三", "四", "五", "上"]
  const types = yao === 1 ? "九" : "六"
  
  if (position === 0) return types + positions[0]
  if (position === 5) return positions[5] + types
  
  return types + positions[position]
}
