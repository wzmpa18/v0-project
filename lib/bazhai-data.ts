// 计算命卦
export function calcMingGua(year: number, gender: "male" | "female"): string {
  const yearSum = year.toString().split("").reduce((a, b) => a + parseInt(b), 0)
  let num = yearSum
  while (num >= 10) {
    num = num.toString().split("").reduce((a, b) => a + parseInt(b), 0)
  }
  
  if (gender === "male") {
    const result = 11 - num
    const index = result > 9 ? result - 9 : result
    return MING_GUA.male[index - 1] || "坎"
  } else {
    const result = num + 4
    const index = result > 9 ? result - 9 : result
    return MING_GUA.female[index - 1] || "坤"
  }
}

// 判断东四命还是西四命
export function getDongXiMing(mingGua: string): "东四命" | "西四命" {
  return DONG_XI_MING.东四命.includes(mingGua) ? "东四命" : "西四命"
}
