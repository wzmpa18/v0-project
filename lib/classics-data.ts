// 搜索辨证函数
export function searchSyndrome(keyword: string): string[] {
  const results: string[] = []
  
  // 六经辨证关键词映射
  const syndromeMap: Record<string, string[]> = {
    "太阳": ["太阳中风", "太阳伤寒", "发热恶寒", "头痛", "项强", "脉浮"],
    "阳明": ["阳明腑实", "潮热", "便秘", "腹胀", "大汗", "口渴", "脉洪大"],
    "少阳": ["少阳病", "往来寒热", "胸胁苦满", "口苦", "咽干", "目眩"],
    "太阴": ["太阴病", "腹满", "吐", "食不下", "自利", "脾虚"],
    "少阴": ["少阴病", "脉微细", "但欲寐", "下利", "厥逆", "恶寒蜷卧"],
    "厥阴": ["厥阴病", "消渴", "气上撞心", "饥不欲食", "吐蛔", "寒热错杂"]
  }
  
  for (const [jing, keywords] of Object.entries(syndromeMap)) {
    if (keywords.some(k => keyword.includes(k) || k.includes(keyword))) {
      results.push(jing)
    }
  }
  
  return results
}
